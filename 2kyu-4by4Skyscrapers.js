function arraysEqual(arr1,arr2) {
    if(arr2 == []){
        return false;
    }
    if(arr1.length !== arr2.length){
        return false;
    }
    for(i=0; i<arr1.length; i++) {
        if(arr1[i] !== arr2[i] && arr2[i] !== 0){
            return false;
        }
    }
    return true;
}


